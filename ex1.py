num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))
# entrada para o usuário



soma = num1 + num2
media = soma / 2
diferenca_absoluta = abs(num1 - num2)
maior = max(num1, num2)
#cálculos


print(f"Soma: {soma}")
print(f"Média: {media}")
print(f"Diferença absoluta: {diferenca_absoluta}")
print(f"Número maior: {maior}")
